#[repr(u16)]
pub enum SyntaxKind {
    ROOT,
    BINARY_EXPR,
    ASSIGNMENT_EXPR,
    PAREN_EXPR,
    IF_EXPR,
    FOR_EXPR,
    WHILE_EXPR,
    FUNCTION_EXPR,
    BLOCK_EXPR,
    UNARY_EXPR,
    IDENT,
    INT,
    FLOAT,
    STRING,
    COMMENT,
    TILDE,
    USER_OP,
    LBRACK,
    RBRACK,
    LBRACK2,
    RBRACK2,
    PLUS,
    MINUS,
    STAR,
    SLASH,
    CARET,
    PIPE,
    COLON,
    COLON2,
    COLON3,
    DOLLAR,
    AT,
    SEMICOLON,
    COMMA,
    OR,
    OR2,
    AND,
    AND2,
    EQUAL2,
    NOT_EQUAL,
    BANG,
    LESS_THAN,
    LESS_THAN_OR_EQUAL,
    GREATER_THAN,
    GREATER_THAN_OR_EQUAL,
    LPAREN,
    RPAREN,
    IF_KW,
    ELSE_KW,
    FOR_KW,
    WHILE_KW,
    FUNCTION_KW,
    IN_KW,
    LBRACE,
    RBRACE,
    WHITESPACE,
    NEWLINE,
    ASSIGN_LEFT,
    SUPER_ASSIGN,
    ASSIGN_RIGHT,
    SUPER_ASSIGN_RIGHT,
    ASSIGN_EQ,
    CALL_EXPR,
    SUBSET_EXPR,
    SUBSET2_EXPR,
    ARG_LIST,
    ARG,
    ERROR,
}

impl From<SyntaxKind> for rowan::SyntaxKind {
    fn from(kind: SyntaxKind) -> Self {
        Self(kind as u16)
    }
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Hash)]
pub enum RLanguage {}

impl Language for RLanguage {
    type Kind = SyntaxKind;

    fn kind_from_raw(raw: rowan::SyntaxKind) -> Self::Kind {
        match raw.0 {
            0 => SyntaxKind::ROOT,
            1 => SyntaxKind::BINARY_EXPR,
            2 => SyntaxKind::ASSIGNMENT_EXPR,
            3 => SyntaxKind::PAREN_EXPR,
            4 => SyntaxKind::IF_EXPR,
            5 => SyntaxKind::FOR_EXPR,
            6 => SyntaxKind::WHILE_EXPR,
            7 => SyntaxKind::FUNCTION_EXPR,
            8 => SyntaxKind::BLOCK_EXPR,
            9 => SyntaxKind::UNARY_EXPR,
            10 => SyntaxKind::IDENT,
            11 => SyntaxKind::INT,
            12 => SyntaxKind::FLOAT,
            13 => SyntaxKind::STRING,
            14 => SyntaxKind::COMMENT,
            15 => SyntaxKind::TILDE,
            16 => SyntaxKind::USER_OP,
            17 => SyntaxKind::LBRACK,
            18 => SyntaxKind::RBRACK,
            19 => SyntaxKind::LBRACK2,
            20 => SyntaxKind::RBRACK2,
            21 => SyntaxKind::PLUS,
            22 => SyntaxKind::MINUS,
            23 => SyntaxKind::STAR,
            24 => SyntaxKind::SLASH,
            25 => SyntaxKind::CARET,
            26 => SyntaxKind::PIPE,
            27 => SyntaxKind::COLON,
            28 => SyntaxKind::COLON2,
            29 => SyntaxKind::COLON3,
            30 => SyntaxKind::DOLLAR,
            31 => SyntaxKind::AT,
            32 => SyntaxKind::SEMICOLON,
            33 => SyntaxKind::COMMA,
            34 => SyntaxKind::OR,
            35 => SyntaxKind::OR2,
            36 => SyntaxKind::AND,
            37 => SyntaxKind::AND2,
            38 => SyntaxKind::EQUAL2,
            39 => SyntaxKind::NOT_EQUAL,
            40 => SyntaxKind::BANG,
            41 => SyntaxKind::LESS_THAN,
            42 => SyntaxKind::LESS_THAN_OR_EQUAL,
            43 => SyntaxKind::GREATER_THAN,
            44 => SyntaxKind::GREATER_THAN_OR_EQUAL,
            45 => SyntaxKind::LPAREN,
            46 => SyntaxKind::RPAREN,
            47 => SyntaxKind::IF_KW,
            48 => SyntaxKind::ELSE_KW,
            49 => SyntaxKind::FOR_KW,
            50 => SyntaxKind::WHILE_KW,
            51 => SyntaxKind::FUNCTION_KW,
            52 => SyntaxKind::IN_KW,
            53 => SyntaxKind::LBRACE,
            54 => SyntaxKind::RBRACE,
            55 => SyntaxKind::WHITESPACE,
            56 => SyntaxKind::NEWLINE,
            57 => SyntaxKind::ASSIGN_LEFT,
            58 => SyntaxKind::SUPER_ASSIGN,
            59 => SyntaxKind::ASSIGN_RIGHT,
            60 => SyntaxKind::SUPER_ASSIGN_RIGHT,
            61 => SyntaxKind::ASSIGN_EQ,
            62 => SyntaxKind::CALL_EXPR,
            63 => SyntaxKind::SUBSET_EXPR,
            64 => SyntaxKind::SUBSET2_EXPR,
            65 => SyntaxKind::ARG_LIST,
            66 => SyntaxKind::ARG,
            67 => SyntaxKind::ERROR,
            _ => SyntaxKind::ERROR,
        }
    }

    fn kind_to_raw(kind: Self::Kind) -> rowan::SyntaxKind {
        kind.into()
    }
}

pub type SyntaxNode = rowan::SyntaxNode<RLanguage>;
